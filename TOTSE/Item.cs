        private byte[] itemBytes;

        public ushort Index
        {
            get
            {
                return BitConverter.ToUInt16(itemBytes, 0x0);
            }
            set
            {
                itemBytes[0x0] = (byte)(value & 0xFF);
                itemBytes[0x1] = (byte)((value >> 8) & 0xFF);
            }
        }
        public byte FreshCount
        {
            get
            {
                return itemBytes[0x3];
            }
            set
            {
                itemBytes[0x3] = value;
            }
        }
        public int Quality
        {
            get
            {
                return BitConverter.ToInt32(itemBytes, 0x4);
            }
            set
            {
                Array.Copy(BitConverter.GetBytes(value), 0, itemBytes, 0x4, 4);
            }
        }
        public byte Quantity
        {
            get
            {
                return itemBytes[0x8];
            }
            set
            {
                itemBytes[0x8] = value;
            }
        }
        public bool IsRegistered
        {
            get
            {
                return itemBytes[0xB] == 0x80;
            }
            set
            {
                if (value)
                    itemBytes[0xB] |= 0x80;
                else
                    itemBytes[0xB] &= 0x7F;
            }
        }


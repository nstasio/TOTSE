        /// Initialize an item by item index
        /// </summary>
        /// <param name="index"></param>
        public Item(ushort index)
        {
            itemBytes = new Item().ToArray();
            Index = index;
            Quality = BaseQuality[index];
            Quantity = 1;
        }

